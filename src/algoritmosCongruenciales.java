
public class algoritmosCongruenciales {
  private int cantidad;

  public algoritmosCongruenciales(int cant){
    this.cantidad = cant;
  }

  public float[] AlgoritmoLineal(){
    float[] numGenerados = new float[cantidad];

    return numGenerados;
  }

  public float[] AlgoritmoMultiplicativo(){
    float[] numGenerados = new float[cantidad];

    return numGenerados;
  }

}
